# Use of if, elif, else
person = 'Stranger'

if person == 'Stranger':
    print("Welcome, what's your name?")
else:
    print('Welcome!')


# Use of if, elif, else
person = 'Imran'
if person == 'Imran':
    print('Welcome Imran!')

elif person =='Munyeem':
    print('Welcome Munyeem!')

else:
    print("Welcome, what's your name?")